$(function () {
  $('.c-menu').click(function () {
    $('.p-foodmenu').toggleClass('open');
    $('.p-foodmenu--background').toggleClass('open');
  });
  $('.c-menu--close').click(function () {
    $('.p-foodmenu').toggleClass('open');
    $('.p-foodmenu--background').toggleClass('open');
  });
});
